//a program to print natural numbers 1 to 10 in descending order.
#include <iostream>
using namespace std;
int main ()
{
	cout << "This program print natural numbers 1 to 10 in descending order.\n";
	for (int n=10;n>0;n--)
	{
		cout<<n<<endl;
	}
	cout<<"That's All\n";
	cout << "=================================";
	return 0;
}
